
function P = LenardJones(a,r,e,d)

a = a*(1+0.035);

switch d
    case 0 % Potentiel
        
        P = e*(0.25*(a/r)^12 - 0.5*(a/r)^6 - 0.3750*(a/r)^10 + 0.75*(a/r)^4 +0.25*2^(-0.5)*(a/r)^9-0.5*2^(-0.5)*(a/r)^3);
        
    case 1 % force
        
        P = e*(3*2^(-1.5)*a^3*r^(-4) - 3*a^4*r^(-5) + 3*a^6*r^(-7) - 9*2^(-0.5)*0.25*a^9*r^(-10) +30*0.1250*a^10*r^(-11)-3*a^12*r^(-13));
        
    case 2 % constante de ressord
        
        r = a;
        
        P = e*(39*a^12*r^(-14) - 41.25*a^10*r^(-12) + 22.5*2^(-0.5)*a^9*r^(-11) - 21*a^6*r^(-8) +15*a^4*r^(-6)-6*2^(-0.5)*a^3*r^(-5));
        
end

end
